#include <stdio.h>


int ft_isalpha(int a)
{
	if (('a' <= a && a <= 'z') || ('A' <= a && a <= 'Z'))
		return (1);
	return (0);
}

int main(void)
{
	printf("%d\n",ft_isalpha('1'));
}